import Foundation

extension UIColor {

  class func primaryColor() -> UIColor {
    return UIColor(red: 76.0/255.0, green: 141.0/255.0, blue: 245.0/255.0, alpha: 1.0)
  }

  class func secondaryColor() -> UIColor {
    return UIColor(red: 245.0/255.0, green: 95.0/255.0, blue: 28.0/255.0, alpha: 1.0)
  }

}
